function establishPortfolioItems(callback) {

    var PortfolioUxContainer = $('#ux-work-holder');
    var PortfolioGraphicContainer = $('#graphic-work-holder');
    var PortfolioMotionContainer = $('#motion-work-holder');

    function getHTML(item) {
        if(item.isFancybox){ var fancybox = ['class="fancybox"', 'data-fancybox="images"']; } else {var fancybox = ['target="_blank"', '']; }
        // col-lg-6 col-md-6 porfolioItem popular
        return `<div class="` + item.dimensions + ` portfolio-item ` + item.group + `" ` + fancybox[1] + `>
        <a ` + fancybox[0] + ` href="` + item.popupLink + `">
     <div class="portfolio-box">
         <div class="portfolio-img">
             <img src="` + item.imageUrl + `" alt="CopComp Logo">
         </div>
         <div class="portfolio-title">
             <div>
                 <h5>` + item.title + `</h5>
                 <span>` + item.category + `</span>
             </div>
         </div>
     </div>
    </a></div>`;

    }

    var outputHTML = [];

    PortfolioUxItems.forEach(function(item) {
        item.popupBackground = "black";
        item.isFancybox = (item.group !== "websites" && item.group !== "mobile-applications");
        outputHTML.push(getHTML(item));
    });
    PortfolioUxContainer.append(outputHTML.join(""));
    outputHTML = [];

    PortfolioGraphicDesignItems.forEach(function(item) {
        item.popupBackground = "white";
        item.isFancybox = true;
        outputHTML.push(getHTML(item));
    });
    PortfolioGraphicContainer.append(outputHTML.join(""));
    outputHTML = [];

    PortfolioMotionDesignItems.forEach(function(item) {
        item.popupBackground = "white";
        item.isFancybox = true;
        outputHTML.push(getHTML(item));
    });
    PortfolioMotionContainer.append(outputHTML.join(""));
    outputHTML = [];

    setTimeout(function() {
        callback();
    }, 300)

}